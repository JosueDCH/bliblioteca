        titulo=txtTitulo.getText().trim();
        editorial=txtEditorial.getText().trim();
        autor=txtAutor.getText().trim();
        year=txtAño.getText().trim();
        idioma=txtIdioma.getText().trim();
        imagen=lblrutaImagen.getText().trim();
        query="INSERT INTO LIBROS (IDLIBRO, TITULO, EDITORIAL, AUTOR, IDIOMA, IMAGEN, YEAR) VALUES(LIBROS_SEQ.NextVal,'"+titulo+"','"+editorial+"','"+autor+"','"+idioma+"','"+imagen+"',"+year+")";
        System.out.println("Query: "+query);
        con.conectar();
        int resp;
        resp=con.Escribir(query);
        
        if(resp==1){
            JOptionPane.showMessageDialog(null, "Se creo correctamente");
        }else{
            JOptionPane.showMessageDialog(null, "Ocurrio un problema");
        }
        con.desconexion();
        
        
