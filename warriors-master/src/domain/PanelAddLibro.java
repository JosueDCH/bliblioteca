    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        //Creamos un objeto de JfileChooser
        JFileChooser dialogo = new JFileChooser();
        //Del objeto creado mandamos a llamar al metodo setFileFilter
        dialogo.setFileFilter(filter);
        //Abrir una ventana de dialogo para escoger la imagen
        int opcion = dialogo.showOpenDialog(this);
        //si hacemos click en el boton abrir 
        if(opcion ==JFileChooser.APPROVE_OPTION){
            //Obtener el nombre del archivo seleccionado
            String file = dialogo.getSelectedFile().getPath();
            //obtener la direccion donde se guardara la imegen
            String filedireccion = dialogo.getSelectedFile().toString();
            lblCaratula.setIcon(new ImageIcon(file));
            //modificamos la imagen
            ImageIcon icon = new ImageIcon(file);
            //extrae la imagen del icono
            Image imagen = icon.getImage();
            //cambiamos el tama;o de la imagen
            Image newimagen = imagen.getScaledInstance(lblCaratula.getWidth(),lblCaratula.getHeight() , Image.SCALE_SMOOTH);
            //Se genera el IMAGEICON con la nueva imagen
            ImageIcon newIcon = new ImageIcon(newimagen);
            lblCaratula.setIcon(newIcon);
            lblrutaImagen.setText(filedireccion);
            rutaImegen="";
            
            
            //Mover la imagen a una carpeta diferente

            Path origenPath = FileSystems.getDefault().getPath(filedireccion);
            Path destinoPath = FileSystems.getDefault().getPath("C:\\Users\\henry\\Documents\\NetBeansProjects\\warriors-master\\img\\Caratulas\\" +
                    txtIdLibro.getText() + "_" + txtTitulo.getText() + "-" + fechaActual()+  ".jpg");

            try {
                Files.copy(origenPath, destinoPath, StandardCopyOption.REPLACE_EXISTING);
            } catch (IOException e) {
                System.err.println(e);
            }
            lblrutaImagen.setText(destinoPath.toString());
            
        }

        
    }//GEN-LAST:event_btnCargarActionPerformed

